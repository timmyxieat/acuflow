// Helper to generate past dates (same as in appointments.ts)
function getPastDate(daysAgo: number, hours: number, minutes: number = 0): Date {
  const date = new Date();
  date.setDate(date.getDate() - daysAgo);
  date.setHours(hours, minutes, 0, 0);
  return date;
}

